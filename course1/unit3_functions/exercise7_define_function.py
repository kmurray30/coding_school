# Just like you can use built-in functions like len() and input(), you can also define your own functions.
# Defining a fuction requires you to use the def keyword, followed by the function name and parameters.
# You also need to use the return keyword to return a value from the function.

# Define your own function with def, parameters, and return

def greet(name):
    message = "Hello " + name
    return message

# Call the function
result = greet("Bobo")
print(result)

