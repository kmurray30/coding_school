# Import brings in code from other modules.
# Think of it like a workbench vs a tool shed:
#  - The tool shed contains a lot of extra tools you might need for specific tasks.
#  - Bringing them to the workbench is like importing a module into your program.

import math

result = math.sqrt(16)
print(result)

# Try another math function
print(math.pow(2, 3))

# Expected output:
# 4.0
# 8.0
