# if lets your program make decisions
# When mentally running through the code, remember to treat the if lines as lines that run like any other line of code.

if True:
    print("This will always run!")
if False:
    print("This will never run!")

# Modify this number
age = 20

if age >= 18:
    print("You can vote")

if age < 18:
    print("You cannot vote")

