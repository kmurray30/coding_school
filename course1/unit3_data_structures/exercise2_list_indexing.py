# Access list elements using square brackets and an index
# Indexes start at 0 (first item is index 0)

fruits = ["apple", "banana", "cherry", "date"]

print(fruits[0])  # First item
print(fruits[1])  # Second item
print(fruits[3])  # Fourth item

# You can use negative indexes to count from the end
print(fruits[-1])  # Last item
print(fruits[-2])  # Second to last item

