# Dictionaries store key-value pairs
# Use curly braces with key: value syntax

person = {
    "name": "Alice",
    "age": 25,
    "city": "Portland"
}

print(person)

# Keys can be strings or numbers
scores = {
    "math": 95,
    "english": 87,
    "science": 92
}

print(scores)

