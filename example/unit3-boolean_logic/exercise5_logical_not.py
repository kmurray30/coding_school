# The 'not' operator flips a boolean value.

# Examples:
print(not True)   # False
print(not False)  # True

# You can combine 'not' with other operations:
is_raining = False
print("It is NOT raining: ", not is_raining)

age = 15
print(not (age >= 18))  # Is the person NOT an adult?

