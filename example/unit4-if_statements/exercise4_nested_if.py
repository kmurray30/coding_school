# You can put if statements inside other if statements!
# This is called "nesting".

weather = "sunny"
temperature = 75

if weather == "sunny":
    if temperature > 70:
        print("Perfect day for the beach!")
    else:
        print("Sunny but a bit cold.")
else:
    print("Not sunny today.")

