# Now we will introduce variables. Variables are simply entities that store values.

# Variables are created by using an equal sign (=).
#   Whatever you type on the left side of the = sign is the variable
#      The variable name can be whatever name you want.
#   Whatever is on the right is the value of the variable.
example_variable = "Hello World"

# Just like you were able to write print("Hello World") before, you can now replace the 
#   text "Hello World" with the variable example_variable, and it will print the value "Hello World".
# So print(example_variable) will print "Hello World".

